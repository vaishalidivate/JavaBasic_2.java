package com.company;
/** Program to equal operator and not equal operators */

public class Loops_3 {
    public static void main(String[] args) {
        int x=25, y=35;

        if(x==y)
            System.out.println("x is equal to y "+x+"="+y);
        else if (x!=y)
            System.out.println("x is not equal to y "+x+"!="+y);
    }
}
